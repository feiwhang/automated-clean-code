def add_numbers(x: int, y: int) -> int:
    """Add two numbers together.

    Args:a
      x (int): a number
      y (int): a number

    Returns:
      (int). The sum of the two numbers.
    """
    return x + y


def multiply_numbers(x: int, y: int) -> int:
    """Multiply two numbers together.

    Args:
      x (int): a number
      y (int): a number

    Returns:
      (int). The product of the two numbers.
    """
    return x * y
